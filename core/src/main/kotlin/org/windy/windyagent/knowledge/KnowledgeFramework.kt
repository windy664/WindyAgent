            # WindyAgent 能做什么

            WindyAgent 是管理 Minecraft 服务器的 AI 助手。管理员用自然语言下指令，它自动调用工具执行。

            ## 核心能力

            - **玩家 / 运营**：查在线、查余额、广播、踢人、在指定子服执行命令等（高危操作受安全策略约束，
