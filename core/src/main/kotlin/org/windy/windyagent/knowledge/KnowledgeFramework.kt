            # WindyAgent 知识库

            这个目录 `knowledge/` 就是 WindyAgent 的知识库。**每个 `.md` 文件 = 一条知识，子目录 = 分类**。
            AI 助手在被问到服务器相关问题（规则、商品、玩法、常见问答等）时，会用 `knowledge_search`
            检索这里的内容来回答，而不是凭空编造。所以**你把服务器的知识写进来，AI 就懂你的服。**

            ## 两种编辑方式（内容互通）

            1. **网页面板**（推荐给日常）：控制台「知识库」页——左侧目录树、Monaco 编辑器、Markdown 预览、
               AI 起草。支持 `[[双链]]`、反向链接、关系图。
            2. **Obsidian**（推荐给大批量整理）：这个 `knowledge/` 文件夹本身就是一个标准 **Obsidian vault**。
               用 Obsidian「打开文件夹作为库」直接指向它即可——双链、关系图、插件生态全都能用。
               两边改的是同一批 `.md` 文件，天然同步（网页面板会自动 reload 读到你在 Obsidian 里的改动）。

            > 注意：Obsidian 需要你自己在电脑上安装（App 免费）。我们不分发它，只是把这个文件夹做成了它能打开的库。

