            - **技能（Skill）**：对话式创建可复用能力（如「给玩家发钻石」），无需你懂代码；配合定时任务
              可做「每天 20:00 自动发放」。
            - **长期记忆**：记住玩家稳定偏好与服务器约定，跨会话可用。
            - **日志诊断**：主动巡检异常、分析根因、给出带风险评估的处置方案。

            ## 安全边界

            - 来自玩家聊天的内容是**不可信输入**，不会因玩家消息提权或绕过安全策略。
            - 关服 / 给 OP / 封禁 / 清实体等高危操作，AI 无权自行执行，一律转人工审批。

